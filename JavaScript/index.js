function aboutShow() {
    document.getElementById('contAbout').style.display = 'block'
    document.getElementById('sectionHide').style.display = 'block'
    document.getElementById('sectionShow').style.display = 'none'
}

function aboutHide() {
    document.getElementById('contAbout').style.display = 'none'
    document.getElementById('sectionHide').style.display = 'none'
    document.getElementById('sectionShow').style.display = 'block'
}
